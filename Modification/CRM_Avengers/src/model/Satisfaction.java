package model;

public class Satisfaction {
	private String Courrier;
	private String Type;
	private String Message;
	private String Presse;
	private String Photo;
	private String EnqueteTerrain;
	private String SondageOnline;
	
	public String getCourrier() {
		return Courrier;
	}
	public void setCourrier(String courrier) {
		Courrier = courrier;
	}
	public String getType() {
		return Type;
	}
	public void setType(String type) {
		Type = type;
	}
	public String getMessage() {
		return Message;
	}
	public void setMessage(String message) {
		Message = message;
	}
	public String getPresse() {
		return Presse;
	}
	public void setPresse(String presse) {
		Presse = presse;
	}
	public String getPhoto() {
		return Photo;
	}
	public void setPhoto(String photo) {
		Photo = photo;
	}
	public String getEnqueteTerrain() {
		return EnqueteTerrain;
	}
	public void setEnqueteTerrain(String enqueteTerrain) {
		EnqueteTerrain = enqueteTerrain;
	}
	public String getSondageOnline() {
		return SondageOnline;
	}
	public void setSondageOnline(String sondageOnline) {
		SondageOnline = sondageOnline;
	}
}
