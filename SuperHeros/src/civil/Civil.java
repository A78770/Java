package civil;

public class Civil {
	
	private String Nom;
	private String Prénom;
	private String Civilité;
	private String Coordonnées;
	private String DateDeNaissance;
	private String Nationalité;
	private String DateDeDécès;
	private String Organisation;
	private String Commentaire;
	private String DateAjout;
	private String DateModification;

	public String getNom() {
		return Nom;
	}
	public void setNom(String nom) {
		Nom = nom;
	}
	public String getPrénom() {
		return Prénom;
	}
	public void setPrénom(String prénom) {
		Prénom = prénom;
	}
	public String getCivilité() {
		return Civilité;
	}
	public void setCivilité(String civilité) {
		Civilité = civilité;
	}
	public String getCoordonnées() {
		return Coordonnées;
	}
	public void setCoordonnées(String coordonnées) {
		Coordonnées = coordonnées;
	}
	public String getDateDeNaissance() {
		return DateDeNaissance;
	}
	public void setDateDeNaissance(String dateDeNaissance) {
		DateDeNaissance = dateDeNaissance;
	}
	public String getNationalité() {
		return Nationalité;
	}
	public void setNationalité(String nationalité) {
		Nationalité = nationalité;
	}
	public String getDateDeDécès() {
		return DateDeDécès;
	}
	public void setDateDeDécès(String dateDeDécès) {
		DateDeDécès = dateDeDécès;
	}
	public String getOrganisation() {
		return Organisation;
	}
	public void setOrganisation(String organisation) {
		Organisation = organisation;
	}
	public String getCommentaire() {
		return Commentaire;
	}
	public void setCommentaire(String commentaire) {
		Commentaire = commentaire;
	}
	public String getDateAjout() {
		return DateAjout;
	}
	public void setDateAjout(String dateAjout) {
		DateAjout = dateAjout;
	}
	public String getDateModification() {
		return DateModification;
	}
	public void setDateModification(String dateModification) {
		DateModification = dateModification;
	}

}
