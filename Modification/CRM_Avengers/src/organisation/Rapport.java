package organisation;

public class Rapport {
	
	private String TitreMission;
	private String Succes;
	private String InterlocuteurAvanger;
	private String InterlocuteurCivil;
	private String Degat;
	private String RapportCrise;
	
	public String getTitreMission() {
		return TitreMission;
	}
	public void setTitreMission(String titreMission) {
		TitreMission = titreMission;
	}
	public String getSucces() {
		return Succes;
	}
	public void setSucces(String succes) {
		Succes = succes;
	}
	public String getInterlocuteurAvanger() {
		return InterlocuteurAvanger;
	}
	public void setInterlocuteurAvanger(String interlocuteurAvanger) {
		InterlocuteurAvanger = interlocuteurAvanger;
	}
	public String getInterlocuteurCivil() {
		return InterlocuteurCivil;
	}
	public void setInterlocuteurCivil(String interlocuteurCivil) {
		InterlocuteurCivil = interlocuteurCivil;
	}
	public String getDegat() {
		return Degat;
	}
	public void setDegat(String degat) {
		Degat = degat;
	}
	public String getRapportCrise() {
		return RapportCrise;
	}
	public void setRapportCrise(String rapportCrise) {
		RapportCrise = rapportCrise;
	}
}
